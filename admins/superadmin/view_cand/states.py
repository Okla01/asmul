"""
Модуль `states.py` — описывает Finite‑State Machine (FSM) для раздела «Участницы».

FSM нужен, чтобы разделить логику бота на упорядоченные шаги:
1. `WaitingInline` — бот ждёт, когда администратор введёт inline‑запрос `ps: …`.
   • Из этого состояния переходят к показу карточки участницы.
   • После показа карточки состояние обычно сбрасывается вручную (`await state.clear()`).

При необходимости добавить новые шаги (например, «редактирование данных»)
можно расширить класс `PSearch` дополнительными атрибутами‑State.
"""

from aiogram.fsm.state import StatesGroup, State


class PSearch(StatesGroup):
    """FSM‑группа для раздела «Поиск и просмотр участницы»."""

    WaitingInline = State()  # Состояние ожидания ввода inline‑поиска «ps: …»
