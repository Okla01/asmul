"""
–ú–æ–¥—É–ª—å `keyboards.py` ‚Äî –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ inline‚Äë–∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Ä–∞–∑–¥–µ–ª–∞ ¬´–£—á–∞—Å—Ç–Ω–∏—Ü—ã¬ª.

–°–æ–¥–µ—Ä–∂–∏—Ç –¥–≤–µ —Ñ—É–Ω–∫—Ü–∏–∏:
1. `entry_kb()`  ‚Äî –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –Ω–∞—á–∞–ª—å–Ω–æ–≥–æ —ç–∫—Ä–∞–Ω–∞ (–ø–æ–∏—Å–∫ + –≤—ã—Ö–æ–¥ –≤ –º–µ–Ω—é).
2. `card_kb()`   ‚Äî –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –∫–∞—Ä—Ç–æ—á–∫–∏ —É—á–∞—Å—Ç–Ω–∏—Ü—ã (–Ω–∞–∑–∞–¥ / –º–µ–Ω—é).

–ö–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Å—Ç—Ä–æ—è—Ç—Å—è —á–µ—Ä–µ–∑ `InlineKeyboardBuilder` –¥–ª—è –≥–∏–±–∫–æ–π –∫–æ–º–ø–æ–Ω–æ–≤–∫–∏.
"""

from aiogram.types import InlineKeyboardMarkup, InlineKeyboardButton
from aiogram.utils.keyboard import InlineKeyboardBuilder


# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 1. –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ ¬´üîç –ù–∞–π—Ç–∏ —É—á–∞—Å—Ç–Ω–∏—Ü—É / üè† –í –º–µ–Ω—é¬ª
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
def entry_kb() -> InlineKeyboardMarkup:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –¥–ª—è —ç–∫—Ä–∞–Ω–∞ –ø–æ–∏—Å–∫–∞.

    ‚Ä¢ –ü–µ—Ä–≤–∞—è –∫–Ω–æ–ø–∫–∞ `switch_inline_query_current_chat` ‚Äî –æ—Ç–∫—Ä—ã–≤–∞–µ—Ç inline‚Äë—Ä–µ–∂–∏–º —Å
      –ø—Ä–µ–¥–∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—ã–º –ø—Ä–µ—Ñ–∏–∫—Å–æ–º `ps: ` (Telegram —Å—Ä–∞–∑—É —Ñ–æ–∫—É—Å–∏—Ä—É–µ—Ç –ø–æ–ª–µ –≤–≤–æ–¥–∞).
    ‚Ä¢ –í—Ç–æ—Ä–∞—è –∫–Ω–æ–ø–∫–∞ ‚Äî –≤–æ–∑–≤—Ä–∞—Ç ¬´üè† –í –º–µ–Ω—é¬ª (callback‚Äëdata `a_menu` —É–∂–µ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç—Å—è –≥–ª–æ–±–∞–ª—å–Ω–æ).
    """
    kb = InlineKeyboardBuilder()

    # üîç –ö–Ω–æ–ø–∫–∞ –¥–ª—è inline‚Äë–ø–æ–∏—Å–∫–∞ (–≤ —Ç–µ–∫—É—â–µ–º —á–∞—Ç–µ), –ø—Ä–µ—Ñ–∏–∫—Å ¬´ps: ¬ª + –ø—Ä–æ–±–µ–ª –¥–∞—ë—Ç –ø–æ–¥—Å–∫–∞–∑–∫—É
    kb.button(text="üîç –ù–∞–π—Ç–∏ —É—á–∞—Å—Ç–Ω–∏—Ü—É", switch_inline_query_current_chat="ps: ")

    # üè†  –ù–∞–∑–∞–¥ –≤ –º–µ–Ω—é
    kb.button(text="üè† –í –º–µ–Ω—é", callback_data="a_menu")

    # adjust(1) ‚Üí –∫–∞–∂–¥–∞—è –∫–Ω–æ–ø–∫–∞ –≤ —Å–≤–æ–µ–π —Å—Ç—Ä–æ–∫–µ
    return kb.adjust(1).as_markup()


# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 2. –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –∫–∞—Ä—Ç–æ—á–∫–∏ —É—á–∞—Å—Ç–Ω–∏—Ü—ã (‚Ü©Ô∏è –ù–∞–∑–∞–¥ / üè† –ú–µ–Ω—é)
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
def card_kb() -> InlineKeyboardMarkup:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –ø–æ–¥ –∫–∞—Ä—Ç–æ—á–∫–æ–π —É—á–∞—Å—Ç–Ω–∏—Ü—ã.

    ‚Ä¢ `‚Ü©Ô∏è –ù–∞–∑–∞–¥` ‚Üí callback `p_participants` (–¥–æ–ª–∂–µ–Ω –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å—Å—è –≤ –º–æ–¥—É–ª–µ‚Äë—Ä–æ–¥–∏—Ç–µ–ª–µ).
    ‚Ä¢ `üè† –í –º–µ–Ω—é` ‚Üí callback `a_menu` (–≥–ª–æ–±–∞–ª—å–Ω—ã–π –≤–æ–∑–≤—Ä–∞—Ç –≤ –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é).
    """
    kb = InlineKeyboardBuilder()
    kb.row(
        InlineKeyboardButton(text="‚Ü©Ô∏è –ù–∞–∑–∞–¥", callback_data="p_participants"),
        InlineKeyboardButton(text="üè† –í –º–µ–Ω—é", callback_data="a_menu"),
    )
    return kb.as_markup()
